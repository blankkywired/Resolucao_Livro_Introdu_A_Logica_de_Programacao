# Exercício 3.7 Faça um programa que peça dois números inteiros. Imprima a soma
# desses dois números na tela.

n1 = float(input('Digite o primeiro numero:\n'))
n2 = float(input('Digite o segundo numero:\n'))

print(f'Valor total: {round((n1 + n2), 2)}' )
# Funcao Round >>> Arredondar o valor numerico, (valor, numero de casas decimais p arredondar apos a virgula)
